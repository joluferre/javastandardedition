package Ejerciciosbasicos;

public class OperadoresIncyDecr1 {

	public static void main(String[] args) {
		char a = 'c';
		char b;

		System.out.println(a++);
		System.out.println(a++);
		System.out.println(++a);
		b = --a;
		System.out.println(++a);
		a++;
		--b;
		System.out.println(b++);
		System.out.println(++b);
		System.out.println(++a);
		System.out.println(b--);
		System.out.println(a);
		System.out.println(b);
	}

}
