package Ejerciciosbasicos;

public class Ejercicio3_ijk {

	public static void main(String[] args) {

		int i = 1, j = 1, k = 1;
		System.out.println(++j);
		System.out.println(k++);
		System.out.println(k + ++j);
		System.out.println(i + j++);
		System.out.println(++i + --j + k--);
	}

}
